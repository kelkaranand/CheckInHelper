
public class Student {

	public String id;
	public String fname;
	public String lname;
	public String sname;
	public String media;
	
	public Student(String id, String fname, String lname, String sname, String media)
	{
		this.id=id;
		this.fname=fname;
		this.lname=lname;
		this.sname=sname;
		this.media=media;
	}
}
